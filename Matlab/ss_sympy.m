function dx = ss_sympy(t,x,Mp,Mw,l,Ip,Iw,km,kp,R,r,g,Va)
dx=zeros(4,1);
dx(1)=x(2);
dx(2)=-(Mp*cos(x(3))*l*r*(Mp*R*g*l*r*sin(x(3))+2*Va*km*r-2*km*kp*x(2))+...
    (Ip+Mp*l^2)*(Mp*R*l*r^2*sin(x(3))*x(4)^2+2*Va*km*r-2*km*kp*x(2)))/...
    (R*Mp^2*cos(x(3))*l^2*r^2-(Ip+Mp*l^2)*(2*Iw+Mp*r^2+2*Mw*r^2));
dx(3)=x(4);
dx(4)=Mp*cos(x(3))*l*r*(Mp*r*l*r^2*sin(x(3))*x(4)^2+2*Va*km*r-...
    2*km*kp*x(2))+(2*Iw+Mp*r^2+2*Mw*r^2)*(Mp*R*g*l*r*sin(x(3))+...
    2*Va*km*r-2*km*kp*x(2))/(R*r*(Mp^2*cos(x(3))^2*l^2*r^2-...
    (Ip+Mp*l^2)*(2*Iw+Mp*r^2+2*Mw*r^2)));
end